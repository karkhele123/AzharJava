class DessendingOrder
{
	public static void main(String[] args) {
		for (char ch = 'Z';ch>= 'A';ch-- ) 
		{
			System.out.print(ch+ " ");
			
		}



		for (char ch = 'z';ch>='a' ;ch-- ) 
		{
			System.out.print(ch+ " ");
		}



		

		for (char ch = 57; ch>= 48; ch-- ) 
		{
			System.out.print(ch+ " ");
		}
	}
}